# 导入使用包功能的方式一：
# 导入：import 包名.模块名
# 使用：包名.模块名.功能
# import my_package.module01
# import my_package.module02
# my_package.module01.ok()
# my_package.module02.hi()


# 导入使用包功能的方式二：
# 导入：from 包名 import 模块
# 使用：模块.功能（使用可以不带包名）
# from my_package import module01
# module01.ok()


# 导入使用包功能的方式三：
# 导入：from 包名.模块 import 函数、类、变量、*(表示导入包的模块的所有功能)
# 使用：功能名（只要使用功能名即可）
