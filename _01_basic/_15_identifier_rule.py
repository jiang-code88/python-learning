"""
    15 python 标识符规范
    python 中对各种变量、函数和类等命名时使用的字符串序列称为标识符。
    标识符由26个英文字母大小写、0-9、_组成，数字不可开头，区分大小写，不可以包含空格，
    不可以使用 python 关键字，但是可以包含。
     - 变量名
     - 函数名
     - 类名
     - python 关键字（保留字）
"""
# 1 变量名
# 变量名全小写字母、0-9、_组成，多个单词使用下划线分开。常量全大写字母。

# 2 函数名
# 变量名全小写字母、0-9、_组成，多个单词使用下划线分开。私有函数以双下划线开头。

# 3 类名
# 类名使用大驼峰命名
#  - 大驼峰命名：多个单词的首字母用大写字母开头
#  - 小驼峰命名：第一个单词的首字母用小写字母开头，后续的单词首字母用大写字母开头

# 4 python 关键字
# python 关键字也叫保留字，不可作为标识符。

